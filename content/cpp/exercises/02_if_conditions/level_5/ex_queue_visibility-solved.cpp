// Άσκηση: Ουρά - Ποιοι Βλέπουν την Πόρτα (Άσκηση 11 από PDF)
// Μέτρα πόσοι μαθητές δεν έχουν ψηλότερο μπροστά τους

#include <iostream>
using namespace std;

int main() {
    double h1, h2, h3, h4, h5;
    double maxSoFar;
    int count = 0;
    
    /*
        5 μαθητές στην ουρά. Ο πρώτος βλέπει πάντα την πόρτα.
        Κάθε επόμενος βλέπει μόνο αν είναι ψηλότερος από
        όλους τους προηγούμενους (δηλαδή από το μέγιστο ύψος μέχρι τώρα).
    */
    
    cout << "=== Ουρά στο Μουσείο ===" << endl;
    cout << "Δώσε τα ύψη των 5 μαθητών (από 1ο έως 5ο):\n" << endl;
    
    // 1ος μαθητής - βλέπει πάντα
    cout << "Ύψος 1ου: ";
    cin >> h1;
    maxSoFar = h1;
    count = 1;  // Ο πρώτος βλέπει πάντα
    
    // 2ος μαθητής
    cout << "Ύψος 2ου: ";
    cin >> h2;
    // Αν είναι ψηλότερος από τον 1ο, βλέπει
    if (h2 > maxSoFar) {
        count = count + 1;
        maxSoFar = h2;
    }
    
    // 3ος μαθητής
    cout << "Ύψος 3ου: ";
    cin >> h3;
    // Αν είναι ψηλότερος από το μέγιστο μέχρι τώρα
    if (h3 > maxSoFar) {
        count = count + 1;
        maxSoFar = h3;
    }
    
    // 4ος μαθητής
    cout << "Ύψος 4ου: ";
    cin >> h4;
    if (h4 > maxSoFar) {
        count = count + 1;
        maxSoFar = h4;
    }
    
    // 5ος μαθητής
    cout << "Ύψος 5ου: ";
    cin >> h5;
    if (h5 > maxSoFar) {
        count = count + 1;
    }
    
    cout << "\nΜαθητές που βλέπουν την πόρτα: " << count << endl;
    
    return 0;
}
