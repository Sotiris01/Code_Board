/*
 * Άσκηση: Μέσος όρος 10 βαθμών
 * Επίπεδο: 2
 * 
 * Περιγραφή:
 * Γράψτε πρόγραμμα που διαβάζει 10 βαθμούς ενός μαθητή
 * και εμφανίζει τον μέσο όρο της βαθμολογίας του.
 * Χρησιμοποιήστε την ίδια μεταβλητή για την είσοδο των 10 βαθμών.
 * 
 * Παράδειγμα:
 * Είσοδος: 15 18 12 16 14 17 19 13 15 11
 * Έξοδος: Μέσος όρος: 15
 */

#include <iostream>
using namespace std;

int main() {
    int grade;          // Μεταβλητή για κάθε βαθμό
    int sum = 0;        // Άθροισμα βαθμών
    double average;     // Μέσος όρος
    
    cout << "Εισάγετε 10 βαθμούς:" << endl;
    
    // Βρόχος for για 10 επαναλήψεις
    for (int i = 1; i <= 10; i++) {
        cout << "Βαθμός " << i << ": ";
        cin >> grade;
        
        // Προσθήκη βαθμού στο άθροισμα
        sum = sum + grade;
    }
    
    // Υπολογισμός μέσου όρου
    average = (double)sum / 10;
    
    cout << "Μέσος όρος: " << average << endl;
    
    return 0;
}
