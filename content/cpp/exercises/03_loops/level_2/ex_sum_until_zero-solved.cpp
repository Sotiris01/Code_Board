/*
 * Άσκηση: Άθροισμα αριθμών μέχρι 0
 * Επίπεδο: 2
 * 
 * Περιγραφή:
 * Γράψτε πρόγραμμα που ζητά από τον χρήστη να εισάγει αριθμούς.
 * Το πρόγραμμα θα τους προσθέτει και θα εμφανίζει το τελικό άθροισμά τους.
 * Η διαδικασία εισαγωγής σταματά όταν ο χρήστης εισάγει τον αριθμό 0.
 * 
 * Παράδειγμα:
 * Είσοδος: 2 5 1 0
 * Έξοδος: Άθροισμα: 8
 * 
 * Υπόδειξη: Το 0 είναι η τιμή φρουρός (sentinel value)
 */

#include <iostream>
using namespace std;

int main() {
    int number;
    int sum = 0;
    
    cout << "Εισάγετε αριθμούς (0 για τερματισμό):" << endl;
    
    // Διαβάζουμε τον πρώτο αριθμό
    cout << "Αριθμός: ";
    cin >> number;
    
    // Βρόχος while που συνεχίζει όσο number != 0
    while (number != 0) {
        // Προσθήκη αριθμού στο άθροισμα
        sum = sum + number;
        
        // Διαβάζουμε τον επόμενο αριθμό
        cout << "Αριθμός: ";
        cin >> number;
    }
    
    cout << "Άθροισμα: " << sum << endl;
    
    return 0;
}
