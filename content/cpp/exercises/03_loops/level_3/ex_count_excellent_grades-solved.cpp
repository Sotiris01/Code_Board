/*
 * Άσκηση: Πλήθος άριστων βαθμών
 * Επίπεδο: 3
 * 
 * Περιγραφή:
 * Γράψτε πρόγραμμα που διαβάζει 26 βαθμούς μαθητών
 * και καταμετρά πόσοι από αυτούς είναι άριστοι (>= 18).
 * 
 * Παράδειγμα:
 * Είσοδος: 15 18 12 19 20 ... (26 βαθμοί)
 * Έξοδος: Πλήθος άριστων: 5
 */

#include <iostream>
using namespace std;

int main() {
    int grade;              // Μεταβλητή για κάθε βαθμό
    int countExcellent = 0; // Πλήθος άριστων
    
    cout << "Εισάγετε 26 βαθμούς:" << endl;
    
    for (int i = 1; i <= 26; i++) {
        cout << "Βαθμός " << i << ": ";
        cin >> grade;
        
        // Αν ο βαθμός είναι άριστος (>= 18), αυξάνουμε το countExcellent
        if (grade >= 18) {
            countExcellent = countExcellent + 1;
        }
    }
    
    cout << "Πλήθος άριστων: " << countExcellent << endl;
    
    return 0;
}
