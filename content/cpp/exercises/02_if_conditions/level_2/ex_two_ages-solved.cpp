// Άσκηση: Σύγκριση Δύο Ηλικιών (Άσκηση 3 από PDF)
// Βρες τη μεγαλύτερη ηλικία και έλεγξε αν και οι δύο είναι >90

#include <iostream>
using namespace std;

int main() {
    int age1, age2;
    
    // Διάβασε τις δύο ηλικίες
    cout << "Δώσε την πρώτη ηλικία: ";
    cin >> age1;
    cout << "Δώσε τη δεύτερη ηλικία: ";
    cin >> age2;
    
    // Έλεγξε αν οι ηλικίες είναι ίσες
    if (age1 == age2) {
        cout << "Έχουν την ίδια ηλικία: " << age1 << " ετών" << endl;
    }
    // Έλεγξε αν η πρώτη είναι μεγαλύτερη
    else if (age1 > age2) {
        cout << "Η μεγαλύτερη ηλικία είναι: " << age1 << " ετών" << endl;
    }
    // Αλλιώς η δεύτερη είναι μεγαλύτερη
    else {
        cout << "Η μεγαλύτερη ηλικία είναι: " << age2 << " ετών" << endl;
    }
    
    // Έλεγξε αν και οι δύο είναι πάνω από 90
    if (age1 > 90 && age2 > 90) {
        cout << "May you both reach 100!" << endl;
    }
    
    return 0;
}
