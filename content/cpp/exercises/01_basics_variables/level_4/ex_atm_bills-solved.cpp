// Άσκηση: ATM - Χαρτονομίσματα (Άσκηση 6 από PDF)
// Υπολόγισε τα χαρτονομίσματα των 50, 20, 10 ευρώ με τον ελάχιστο αριθμό

#include <iostream>
using namespace std;

int main() {
    int amount, remaining;
    int bills_50, bills_20, bills_10;
    
    // Διάβασε το ποσό ανάληψης (πολλαπλάσιο του 10)
    cout << "Ποσό ανάληψης (πολλαπλάσιο του 10): ";
    cin >> amount;
    
    remaining = amount;
    
    // Υπολόγισε πόσα 50άρικα χρειάζονται (ακέραια διαίρεση με 50)
    bills_50 = remaining / 50;
    
    // Υπολόγισε το υπόλοιπο μετά τα 50άρικα
    remaining = remaining % 50;
    
    // Υπολόγισε πόσα 20άρικα χρειάζονται
    bills_20 = remaining / 20;
    
    // Υπολόγισε το υπόλοιπο μετά τα 20άρικα
    remaining = remaining % 20;
    
    // Υπολόγισε πόσα 10άρικα χρειάζονται
    bills_10 = remaining / 10;
    
    cout << "\n=== Χαρτονομίσματα ===" << endl;
    cout << bills_50 << " χαρτονομίσματα των 50 ευρώ" << endl;
    cout << bills_20 << " χαρτονομίσματα των 20 ευρώ" << endl;
    cout << bills_10 << " χαρτονομίσματα των 10 ευρώ" << endl;
    
    // Παράδειγμα: 190€ = 3x50 + 2x20 + 0x10
    
    return 0;
}
