/**
 * Επιστρέφει όλες τις λέξεις-κλειδιά σε μια επίπεδη λίστα
 * @returns {string[]} Πίνακας με όλες τις λέξεις-κλειδιά
 */
function getAllKeywords() {
    const all = [];
    for (const category in GLOSSA_KEYWORDS) {
        all.push(...GLOSSA_KEYWORDS[category]);
    }
    // Remove duplicates
    return [...new Set(all)];
}

/**
 * Επιστρέφει όλες τις λέξεις-κλειδιά που χρησιμοποιούνται για syntax highlighting
 * (εξαιρούνται τα σύμβολα)
 * @returns {string[]} Πίνακας με τις λέξεις-κλειδιά για highlighting
 */
function getHighlightableKeywords() {
    const highlightable = [];
    const excludeCategories = ['comparisonOperators', 'specialSymbols'];
    
    for (const category in GLOSSA_KEYWORDS) {
        if (!excludeCategories.includes(category)) {
            highlightable.push(...GLOSSA_KEYWORDS[category]);
        }
    }
    return [...new Set(highlightable)];
}

/**
 * Ελέγχει αν μια λέξη είναι δεσμευμένη λέξη-κλειδί
 * @param {string} word - Η λέξη προς έλεγχο
 * @returns {boolean} true αν είναι λέξη-κλειδί
 */
function isKeyword(word) {
    return getAllKeywords().includes(word);
}

/**
 * Επιστρέφει την κατηγορία μιας λέξης-κλειδιού
 * @param {string} word - Η λέξη-κλειδί
 * @returns {string|null} Η κατηγορία ή null αν δεν βρέθηκε
 */
function getKeywordCategory(word) {
    for (const category in GLOSSA_KEYWORDS) {
        if (GLOSSA_KEYWORDS[category].includes(word)) {
            return category;
        }
    }
    return null;
}

