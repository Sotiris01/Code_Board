/*
 * Άσκηση: Μέγιστο ύψος 6 φίλων
 * Επίπεδο: 3
 * 
 * Περιγραφή:
 * Γράψτε πρόγραμμα που ζητά τα ύψη έξι φίλων
 * και εμφανίζει το μεγαλύτερο ύψος.
 * Χρησιμοποιήστε την ίδια μεταβλητή (height) για την είσοδο των 6 υψών.
 * 
 * Παράδειγμα:
 * Είσοδος: 1.70 1.65 1.82 1.75 1.68 1.79
 * Έξοδος: Το μεγαλύτερο ύψος είναι: 1.82
 */

#include <iostream>
using namespace std;

int main() {
    double height;      // Μεταβλητή για κάθε ύψος
    double maxHeight;   // Το μέγιστο ύψος
    
    cout << "Εισάγετε τα ύψη 6 φίλων:" << endl;
    
    // Διαβάζουμε το πρώτο ύψος και το θεωρούμε μέγιστο
    cout << "Ύψος 1: ";
    cin >> height;
    maxHeight = height;
    
    // Διαβάζουμε τα υπόλοιπα 5 ύψη
    for (int i = 2; i <= 6; i++) {
        cout << "Ύψος " << i << ": ";
        cin >> height;
        
        // Αν το νέο ύψος είναι μεγαλύτερο, ενημερώνουμε το maxHeight
        if (height > maxHeight) {
            maxHeight = height;
        }
    }
    
    cout << "Το μεγαλύτερο ύψος είναι: " << maxHeight << endl;
    
    return 0;
}
