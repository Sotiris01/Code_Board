/*
 * Άσκηση: Δυνάμεις του 2 μέχρι 8192
 * Επίπεδο: 5
 * 
 * Περιγραφή:
 * Γράψτε πρόγραμμα που εμφανίζει τις διαδοχικές δυνάμεις του 2
 * μέχρι την τιμή 8192.
 * Επίσης να εμφανίζει τον εκθέτη κ της τελευταίας δύναμης (2^κ = 8192).
 * 
 * Έξοδος:
 * 2^0 = 1
 * 2^1 = 2
 * 2^2 = 4
 * ...
 * 2^13 = 8192
 * Ο εκθέτης της τελευταίας δύναμης είναι: 13
 */

#include <iostream>
using namespace std;

int main() {
    int power = 1;      // Αρχίζουμε με 2^0 = 1
    int exponent = 0;   // Εκθέτης
    
    cout << "Δυνάμεις του 2 μέχρι 8192:" << endl;
    
    // Βρόχος while όσο η δύναμη είναι <= 8192
    while (power <= 8192) {
        cout << "2^" << exponent << " = " << power << endl;
        
        // Υπολογισμός επόμενης δύναμης (πολλαπλασιασμός x2)
        power = power * 2;
        exponent = exponent + 1;
    }
    
    // Ο τελευταίος εκθέτης που εμφανίστηκε ήταν exponent - 1
    cout << "\nΟ εκθέτης της τελευταίας δύναμης είναι: " << exponent - 1 << endl;
    
    return 0;
}
