// Άσκηση: Τα Μήλα της Μαρίας (από το PDF)
// Η Μαρία έχει 3 μήλα. Ο Γιάννης της δίνει άλλα 2.
// Υπολόγισε πόσα μήλα έχει τώρα η Μαρία.

#include <iostream>
using namespace std;

int main() {
    // Δηλώνουμε ακέραια μεταβλητή
    int sum;
    
    // Υπολογίζουμε το άθροισμα
    sum = 3 + 2;
    
    // Εμφανίζουμε το αποτέλεσμα
    cout << "Η Μαρία έχει " << sum << " μήλα" << endl;
    
    return 0;
}
