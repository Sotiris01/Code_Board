// Άσκηση: Αντιμετάθεση Μεταβλητών (Swap)
// Αντάλλαξε τις τιμές δύο μεταβλητών

#include <iostream>
using namespace std;

int main() {
    int a, b;
    
    cout << "Δώσε τον πρώτο αριθμό: ";
    cin >> a;
    cout << "Δώσε τον δεύτερο αριθμό: ";
    cin >> b;
    
    cout << "Πριν: a=" << a << ", b=" << b << endl;
    
    // Αντιμετάθεση με βοηθητική μεταβλητή
    // Βήμα 1: Αποθηκεύουμε το a
    int temp = a;
    
    // Βήμα 2: Βάζουμε το b στο a
    a = b;
    
    // Βήμα 3: Βάζουμε το temp (παλιό a) στο b
    b = temp;
    
    cout << "Μετά: a=" << a << ", b=" << b << endl;
    
    return 0;
}
