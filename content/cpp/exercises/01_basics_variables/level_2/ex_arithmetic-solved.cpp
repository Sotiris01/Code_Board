// Άσκηση: Αριθμητικές Πράξεις
// Διάβασε δύο αριθμούς και υπολόγισε τις 4 βασικές πράξεις

#include <iostream>
using namespace std;

int main() {
    // Δηλώνουμε δύο ακέραιες μεταβλητές
    int a, b;
    
    cout << "Δώσε τον πρώτο αριθμό: ";
    cin >> a;
    cout << "Δώσε τον δεύτερο αριθμό: ";
    cin >> b;
    
    // Υπολογίζουμε και εμφανίζουμε το άθροισμα
    cout << "Άθροισμα: " << a + b << endl;
    
    // Υπολογίζουμε και εμφανίζουμε τη διαφορά
    cout << "Διαφορά: " << a - b << endl;
    
    // Υπολογίζουμε και εμφανίζουμε το γινόμενο
    cout << "Γινόμενο: " << a * b << endl;
    
    // Υπολογίζουμε και εμφανίζουμε το πηλίκο
    cout << "Πηλίκο: " << a / b << endl;
    
    return 0;
}
