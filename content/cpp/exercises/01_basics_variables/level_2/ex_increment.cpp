// Άσκηση: Προσαύξηση και Μείωση (++x vs x++)
// Δείξε τη διαφορά μεταξύ πρότερης και ύστερης προσαύξησης

#include <iostream>
using namespace std;

int main() {
    int x = 5;
    int y = 5;
    
    // TODO: Εμφάνισε την αρχική τιμή του x
    cout << "Αρχική τιμή x = " << __________ << endl;
    
    // Ύστερη προσαύξηση (post-increment): πρώτα χρησιμοποιεί, μετά αυξάνει
    cout << "Ύστερη προσαύξηση x++: " << __________ << endl;
    cout << "Τιμή x μετά: " << x << endl;
    
    cout << endl;
    
    // TODO: Εμφάνισε την αρχική τιμή του y
    cout << "Αρχική τιμή y = " << __________ << endl;
    
    // Πρότερη προσαύξηση (pre-increment): πρώτα αυξάνει, μετά χρησιμοποιεί
    cout << "Πρότερη προσαύξηση ++y: " << __________ << endl;
    cout << "Τιμή y μετά: " << y << endl;
    
    cout << endl;
    
    // Μείωση (decrement)
    int z = 10;
    cout << "Αρχική τιμή z = " << z << endl;
    // TODO: Χρησιμοποίησε z-- για ύστερη μείωση
    cout << "Ύστερη μείωση z--: " << __________ << endl;
    cout << "Τιμή z μετά: " << z << endl;
    
    return 0;
}
