/*
 * Άσκηση: Περιττοί αριθμοί 1-99
 * Επίπεδο: 2
 * 
 * Περιγραφή:
 * Γράψτε πρόγραμμα που εμφανίζει όλους τους περιττούς αριθμούς
 * από το 1 μέχρι το 99.
 * 
 * Έξοδος:
 * 1 3 5 7 9 11 13 ... 95 97 99
 * 
 * Υπόδειξη: Χρησιμοποιήστε βρόχο for με βήμα 2
 */

#include <iostream>
using namespace std;

int main() {
    cout << "Περιττοί αριθμοί από 1 έως 99:" << endl;
    
    // Βρόχος for που ξεκινά από 1 και αυξάνεται κατά 2
    for (int i = 1; i <= 99; i = i + 2) {
        cout << i << " ";
    }
    
    cout << endl;
    
    return 0;
}
