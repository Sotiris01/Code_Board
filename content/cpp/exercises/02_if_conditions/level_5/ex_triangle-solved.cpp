// Άσκηση: Έλεγχος Τριγώνου
// Έλεγξε αν 3 πλευρές μπορούν να σχηματίσουν τρίγωνο και τι είδους

#include <iostream>
using namespace std;

int main() {
    double a, b, c;
    
    cout << "Δώσε τις 3 πλευρές του τριγώνου: ";
    cin >> a >> b >> c;
    
    // Έλεγχος εγκυρότητας τριγώνου
    // Κανόνας: κάθε πλευρά < άθροισμα των άλλων δύο
    if (a + b > c && b + c > a && a + c > b) {
        cout << "Είναι έγκυρο τρίγωνο!" << endl;
        
        // Έλεγχος τύπου τριγώνου
        if (a == b && b == c) {
            cout << "Τύπος: Ισόπλευρο" << endl;
        }
        else if (a == b || b == c || a == c) {
            cout << "Τύπος: Ισοσκελές" << endl;
        }
        else {
            cout << "Τύπος: Σκαληνό" << endl;
        }
    }
    else {
        cout << "Δεν μπορούν να σχηματίσουν τρίγωνο!" << endl;
    }
    
    return 0;
}
