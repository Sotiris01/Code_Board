// Άσκηση: Συντομευμένοι Τελεστές (+=, -=, *=, /=)
// Χρησιμοποίησε τους συντομευμένους τελεστές αριθμητικών πράξεων

#include <iostream>
using namespace std;

int main() {
    int x = 20;
    
    cout << "Αρχική τιμή x = " << x << endl;
    cout << endl;
    
    // Πρόσθεσε 5 στο x χρησιμοποιώντας +=
    x += 5;
    cout << "Μετά από x += 5: x = " << x << endl;
    
    // Αφαίρεσε 3 από το x χρησιμοποιώντας -=
    x -= 3;
    cout << "Μετά από x -= 3: x = " << x << endl;
    
    // Πολλαπλασίασε το x με 2 χρησιμοποιώντας *=
    x *= 2;
    cout << "Μετά από x *= 2: x = " << x << endl;
    
    // Διαίρεσε το x με 4 χρησιμοποιώντας /=
    x /= 4;
    cout << "Μετά από x /= 4: x = " << x << endl;
    
    cout << endl;
    
    // Παράδειγμα με υπόλοιπο διαίρεσης
    int y = 17;
    cout << "y = " << y << endl;
    // Βρες το υπόλοιπο της διαίρεσης του y με 5 χρησιμοποιώντας %=
    y %= 5;
    cout << "Μετά από y %= 5: y = " << y << endl;
    
    return 0;
}
