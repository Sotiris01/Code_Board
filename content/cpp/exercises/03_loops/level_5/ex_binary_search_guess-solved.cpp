/*
 * Άσκηση: Binary Search - Ο υπολογιστής μαντεύει τον αριθμό
 * Επίπεδο: 5
 * 
 * Περιγραφή:
 * Το πρόγραμμα ζητά από τον παίκτη να επιλέξει έναν μυστικό αριθμό 1-100.
 * Στη συνέχεια "μαντεύει" αριθμούς και ο παίκτης απαντά:
 * - Y αν είναι σωστός
 * - H αν ο μυστικός αριθμός είναι μεγαλύτερος (Higher)
 * - L αν ο μυστικός αριθμός είναι μικρότερος (Lower)
 * 
 * Χρησιμοποιεί δυαδική αναζήτηση για να βρει τον αριθμό γρήγορα.
 * 
 * Υπόδειξη: Μαντεύει τον μεσαίο αριθμό και μειώνει το εύρος κάθε φορά.
 */

#include <iostream>
using namespace std;

int main() {
    int low = 1, high = 100;
    int guess;
    char answer;
    int attempts = 0;
    
    cout << "Σκέψου έναν αριθμό από 1 έως 100." << endl;
    cout << "Θα προσπαθήσω να τον μαντέψω!" << endl;
    cout << "Απάντησε: Y=Σωστό, H=Μεγαλύτερος, L=Μικρότερος" << endl;
    cout << endl;
    
    // Βρόχος που συνεχίζει μέχρι να βρεθεί ο αριθμός
    do {
        // Υπολογισμός μεσαίου αριθμού (binary search)
        guess = (low + high) / 2;
        attempts = attempts + 1;
        
        cout << "Είναι ο αριθμός " << guess << "? (Y/H/L): ";
        cin >> answer;
        
        // Ανάλογα με την απάντηση, περιορίζουμε το εύρος
        if (answer == 'H' || answer == 'h') {
            // Ο αριθμός είναι μεγαλύτερος, αύξησε το κάτω όριο
            low = guess + 1;
        } else if (answer == 'L' || answer == 'l') {
            // Ο αριθμός είναι μικρότερος, μείωσε το άνω όριο
            high = guess - 1;
        }
        
    } while (answer != 'Y' && answer != 'y');
    
    cout << "\nΒρήκα τον αριθμό " << guess << " με " << attempts << " μαντεψιές!" << endl;
    
    return 0;
}
