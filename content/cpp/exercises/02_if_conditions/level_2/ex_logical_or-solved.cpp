// Άσκηση: Λογικός Τελεστής OR (||)
// Έλεγξε αν ένας αριθμός είναι εκτός ορίων

#include <iostream>
using namespace std;

int main() {
    int number;
    
    cout << "Δώσε έναν αριθμό: ";
    cin >> number;
    
    // Έλεγχος αν είναι εκτός ορίων με OR (||)
    // True αν ΟΠΟΙΑΔΗΠΟΤΕ συνθήκη είναι true
    if (number < 1 || number > 100) {
        cout << "Ο αριθμός είναι ΕΚΤΟΣ ορίων (1-100)" << endl;
    }
    else {
        cout << "Ο αριθμός είναι ΕΝΤΟΣ ορίων (1-100)" << endl;
    }
    
    // Έλεγχος αν είναι Σαββατοκύριακο
    int day;
    cout << "\nΔώσε ημέρα (1=Δευτέρα, 7=Κυριακή): ";
    cin >> day;
    
    // Σαββατοκύριακο = ημέρα 6 Ή ημέρα 7
    if (day == 6 || day == 7) {
        cout << "Είναι Σαββατοκύριακο!" << endl;
    }
    else {
        cout << "Είναι καθημερινή." << endl;
    }
    
    return 0;
}
